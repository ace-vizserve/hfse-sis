-- =====================================================================
-- 2. Backfill RLS + policy on every existing ay{YY}_* admissions table
-- =====================================================================
--
-- Walks `pg_tables` for the 4 suffixes across every AY slug already in
-- the public schema. Enables RLS if it isn't already. Adds the canonical
-- policy if it isn't already present. Existing AY2026 policy is matched
-- by name and left alone.

do $$
declare
  v_row record;
begin
  for v_row in
    select schemaname, tablename
    from pg_tables
    where schemaname = 'public'
      and tablename ~ '^ay[0-9]{2}_(enrolment_applications|enrolment_status|enrolment_documents|discount_codes)$'
  loop
    execute format(
      'alter table %I.%I enable row level security',
      v_row.schemaname, v_row.tablename
    );

    if not exists (
      select 1 from pg_policies
      where schemaname = v_row.schemaname
        and tablename = v_row.tablename
        and policyname = 'Policy with security definer functions'
    ) then
      execute format($pol$
        create policy "Policy with security definer functions"
        on %I.%I
        for all
        to public
        using (true)
        with check (true);
      $pol$, v_row.schemaname, v_row.tablename);
    end if;
  end loop;
end $$;
