-- 025_ay_tables_rls.sql
--
-- Aligns wizard-created `ay{YY}_*` admissions tables with the production
-- AY2026 security model: RLS enabled + a single permissive policy named
-- "Policy with security definer functions" granting ALL to public. This
-- mirrors how the Supabase UI generated the original AY2026 setup and is
-- what the parent portal + SIS both expect.
--
-- Before this migration: `create_ay_admissions_tables` (from migration 012)
-- created the 4 per-AY tables but didn't enable RLS. Wizard-created AYs
-- (AY2027+ and the AY9999 test environment) were therefore wide-open in
-- a different way than AY2026 — in practice the SIS uses service-role
-- everywhere so it didn't matter, but the schemas drifted and any future
-- policy-based access would have behaved inconsistently per AY.
--
-- Scope:
--   1. Rewrite `create_ay_admissions_tables` to enable RLS + add the
--      permissive policy on each of the 4 new tables inside the same
--      function call. Idempotent — `if not exists` guards + pg_policies
--      pre-check.
--   2. Backfill every existing `ay{YY}_{enrolment_applications,
--      enrolment_status, enrolment_documents, discount_codes}` table in
--      the public schema. Enables RLS if it isn't already; adds the
--      policy if it isn't already present. AY2026's existing setup is
--      preserved because the policy name already matches.
--
-- Apply after 024. Safe to re-run.

