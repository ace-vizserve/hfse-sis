-- 026_ay_slug_4digit.sql
--
-- Normalizes the AY admissions-table naming convention to `ay{YYYY}` (e.g.
-- `ay2026_enrolment_applications`), matching the existing production
-- AY2026 tables and every read-side helper in `lib/` (`queries.ts`,
-- `dashboard.ts`, the `app/api/sis/students/*` routes, the parent portal,
-- etc.).
--
-- Before this migration:
--   - `create_ay_admissions_tables` + `drop_ay_admissions_tables` validated
--     `^ay[0-9]{2}$`, so they accepted `ay27`, `ay99` and rejected `ay2026`.
--   - `create_academic_year` / `delete_academic_year` computed the slug as
--     `substring(v_code from 5 for 2)` → 2-digit (`ay27`), so wizard-created
--     AYs produced tables no read-side code would ever find.
--
-- After this migration:
--   - All four RPCs accept/emit 4-digit slugs (`^ay[0-9]{4}$`, `ay2027`).
--   - AY2026 production tables are untouched (validator still matches them).
--   - Orphan 2-digit tables in this DB (`ay99_*`, `ay27_*`) are auto-dropped
--     iff they contain zero rows. Protects against any hypothetical data.
--
-- Apply after 025. Safe to re-run; every function uses CREATE OR REPLACE
-- and the cleanup block is idempotent.

