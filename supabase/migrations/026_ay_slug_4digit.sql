-- 5. One-shot cleanup: drop orphan 2-digit admissions tables
-- =====================================================================
--
-- Any `ay{YY}_(enrolment_applications|enrolment_status|enrolment_documents|
-- discount_codes)` tables that exist in this DB were created by the
-- pre-026 wizard/seeder. The read-side code has always computed 4-digit
-- slugs, so these tables have never been read and should be empty. We
-- drop only the empty ones; any table with rows is left alone and logged
-- as a notice so a human can investigate.
--
-- This block is a no-op in databases that never ran the wizard / test env
-- (e.g. a fresh clone). In the dev DB that has `ay99_*` from prior test
-- switches, it wipes those orphans so the next switch-to-Test can create
-- `ay9999_*` cleanly.

do $$
declare
  v_row record;
  v_rowcount bigint;
begin
  for v_row in
    select schemaname, tablename
    from pg_tables
    where schemaname = 'public'
      and tablename ~ '^ay[0-9]{2}_(enrolment_applications|enrolment_status|enrolment_documents|discount_codes)$'
  loop
    execute format('select count(*) from %I.%I', v_row.schemaname, v_row.tablename)
      into v_rowcount;

    if v_rowcount = 0 then
      execute format('drop table if exists %I.%I cascade', v_row.schemaname, v_row.tablename);
      raise notice '[026] dropped orphan 2-digit table %.% (empty)',
        v_row.schemaname, v_row.tablename;
    else
      raise notice '[026] preserved %.% — has % rows (manual review required)',
        v_row.schemaname, v_row.tablename, v_rowcount;
    end if;
  end loop;
end $$;
